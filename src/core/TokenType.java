package core;

enum TokenType {
    ID("(?<=[=+\\-%&*]|\\s|^)(\\$[a-z]+[_\\d\\w]+)(?=[=\\+\\-%&\\*]|\\s|$)"),
    NUMINT("(?<=[=+\\-%&*]|\\s|^)(\\d+)(?=[=\\+\\-%&\\*]|\\s|$)"),
    NUMREAL("(?<=[=+\\-%&*]|\\s|^)(/\\d+,\\d+/)(?=[=\\+\\-%&\\*]|\\s|$)"),
    NUMHEX("(?<=[=+\\-\\%\\&*]|\\s|^)([0-9A-F]+/)(?=[=\\+\\-\\%\\&\\*]|\\s|$)"),
    STRING("(?<=[=+\\-%&*]|\\s|^)(\".*\"/)(?=[=\\+\\-%&\\*]|\\s|$)"),
    OP("(?<=.)([=\\+\\-%&]|(\\*{1,2}))(?=.)");
    public final String pattern;

    TokenType(String pattern) {
        this.pattern = pattern;
    }
}
