package core;

enum TokenType {
    ID("(?<=[=+\\-%&*]|\\s|^)(\\$[a-z]+[_\\d\\w]+)(?=[=\\+\\-%&\\*]|\\s|$)"),
    NUM_INT("(?<=[=+\\-%&*]|\\s|^)(\\d+)(?=[=\\+\\-%&\\*]|\\s|$)"),
    NUM_REAL("(?<=[=+\\-%&*]|\\s|^)(/\\d+,\\d+/)(?=[=\\+\\-%&\\*]|\\s|$)"),
    NUM_HEX("(?<=[=+\\-%&*]|\\s|^)([0-9A-F]+/)(?=[=\\+\\-%&\\*]|\\s|$)"),
    STRING("(?<=[=+\\-%&*]|\\s|^)(\".*\"/)(?=[=\\+\\-%&\\*]|\\s|$)"),
    OP("(?<=.)([=\\+\\-%&]|(\\*{1,2}))(?=.)");
    public final String pattern;

    TokenType(String pattern) {
        this.pattern = pattern;
    }
}
